package APOO.praticas;
public class AreaTriangulo {

    public static void main(String[] args) {
       // declaração da variáveis
int comprimento = 4;
int largura = 6;
int area;

// Calcula a area do triangulo
area = comprimento * largura;

// impressão das variaveis
System.out.println("Se o comprimento é: " + comprimento);
System.out.println("E a largura é: " + largura);
System.out.println("Então a area do retangulo é: " + area);
    }
}
